def write_name():
    name = input('Введите имя: ').capitalize()
    return name


def write_surname():
    surname = input('Введите фамилию: ').capitalize()
    return surname


def write_phone():
    phone = input('Введите телефон: ')
    return phone


def write_adress():
    adress = input('Введите адрес: ').title()
    return adress